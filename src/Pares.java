
public class Pares {
	double si,no,total;
	
	public Pares(double s,double n){
		si=s;
		no=n;
		total=s+n;
	}
	
	public double getSi() {
		return si;
	}
	public void setSi(double si) {
		this.si = si;
	}
	public double getNo() {
		return no;
	}
	public void setNo(double no) {
		this.no = no;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
}
